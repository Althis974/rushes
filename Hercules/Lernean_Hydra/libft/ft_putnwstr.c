#include "libft.h"

int		ft_putnwstr(wchar_t const *s, size_t len)
{
	size_t	i;

	i = 0;
	if (s)
	{
		while (i < len)
		{
			ft_putwchar(s[i]);
			i++;
		}
	}
	return (i);
}
