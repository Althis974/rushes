#include "libft.h"

int		ft_str_is_numeric(const char *s)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			if (ft_isdigit(s[i]) == 0)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
