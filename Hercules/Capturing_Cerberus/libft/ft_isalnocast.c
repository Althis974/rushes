#include "libft.h"

int		ft_isalnocast(int c)
{
	if (c == '%' || (c >= 65 && c <= 90) || (c >= 97 && c <= 103) || c == 105
			|| c == 107 || (c >= 109 && c <= 121))
		return (1);
	return (0);
}
